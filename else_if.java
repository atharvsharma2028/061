public class else_if {
    public static void main(String args[]) {
        int a = 25;
        if (a > 0) {
            System.out.println("a is positive");
        } else if (a < 0) {
            System.out.println("a is negative");
        } else {
            System.out.println("a is zero");
        }

    }
    
}
